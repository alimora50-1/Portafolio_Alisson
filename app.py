import streamlit as st



st.title("Análisis de la Base de Datos Northwind")  

st.write("""
Este proyecto explora los datos de la base de datos **Northwind**, que contiene información valiosa sobre ventas, inventarios, clientes y logística.  
El objetivo es analizar dinámicas clave del negocio a través de visualizaciones interactivas, como gráficos de dispersión y mapas jerárquicos, para ofrecer insights que apoyen la toma de decisiones estratégicas.  



### Propósito del Proyecto  
A través de los siguientes análisis visuales, se busca:
- **Relación entre precios de productos y valor de las órdenes**: Utilizando un gráfico de dispersión, 
se analiza cómo los precios de los productos impactan
el valor de las órdenes de compra, proporcionando información clave sobre patrones de consumo.  
- **Distribución de pedidos por región de envío**: A través de un mapa jerárquico, 
se examina la cantidad acumulada de pedidos por región, 
identificando áreas con mayor demanda y optimizando las estrategias logísticas.  

Estos análisis no solo ayudan a entender las tendencias de venta y logística, 
 sino que también permiten identificar áreas de mejora en la toma de decisiones comerciales.  


Mi enfoque está en aplicar el análisis de datos para resolver problemas empresariales y generar valor estratégico en el entorno corporativo.  
""")
